#include "p_s.h"

void	rra(t_pile *a)
{
	int	i;

	i = a->size - 1;
	a->tmp = a->pile[a->size - 1];
	while (i > 0)
	{
		a->pile[i] = a->pile[i - 1];
		i--;
	}
	a->pile[i] = a->tmp;
	write(1, "rra\n", 4);
}
