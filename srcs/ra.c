#include "p_s.h"

void	ra(t_pile *a)
{
	int	i;

	i = 0;
	a->tmp = a->pile[0];
	while (i < a->size - 1)
	{
		a->pile[i] = a->pile[i + 1];
		i++;
	}
	a->pile[i] = a->tmp;
	write(1, "ra\n", 3);
}
