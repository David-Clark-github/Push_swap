#include "p_s.h"

void	rb(t_pile *b)
{
	int	i;

	i = 0;
	b->tmp = b->pile[0];
	while (i < b->size - 1)
	{
		b->pile[i] = b->pile[i + 1];
		i++;
	}
	b->pile[i] = b->tmp;
	write(1, "rb\n", 3);
}
