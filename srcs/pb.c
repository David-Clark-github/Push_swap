#include "p_s.h"

void	pb(t_pile *a, t_pile *b)
{
	int	i;

	if (a->size > 0)
	{
		b->size++;
		i = b->size - 1;
		while (i != 0)
		{
			b->pile[i] = b->pile[i - 1];
			i--;
		}
		b->pile[0] = a->pile[0];
		a->size--;
		while (i < a->size)
		{
			a->pile[i] = a->pile[i + 1];
			i++;
		}
	}
	write(1, "pb\n", 3);
}
