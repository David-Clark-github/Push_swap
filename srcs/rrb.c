#include "p_s.h"

void	rrb(t_pile *b)
{
	int	i;

	i = b->size - 1;
	b->tmp = b->pile[b->size - 1];
	while (i > 0)
	{
		b->pile[i] = b->pile[i - 1];
		i--;
	}
	b->pile[i] = b->tmp;
	write(1, "rrb\n", 4);
}
